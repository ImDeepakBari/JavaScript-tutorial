//while loop
const runs = 10
let index = 0

while(index < runs ){
    console.log(`Index is ${index}`);
    index = index + 2
}

myArray = ["terminator", 'antman', "Hulk"]
let i = 0
while(i < myArray.length){
    console.log(`Value is: ${myArray[i]}`);
    i = i + 1
}

// do-while loop
let score = 1
do{
    console.log(`score is ${score}`)
    score++
}while(score < 10);
 