const month = "Jan"

switch(month){
    case "Jan":
        console.log("Execution for Jan")
        break;
    case "Feb":
        console.log("Execution for Feb")
        break;
    case "Mar":
        console.log("Execution for Mar")
        break;
    case "April":
        console.log("Execution for Apr")
        break;
    case "Dec":
        console.log("Execution for Dec")
        break;

    default:
        console.log("No match found!!")
        break;
        
}